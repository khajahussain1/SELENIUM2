package s;

public class Switch {

	public static void main(String[] args) {
		int n=20;
		
		switch(n)
		{
		case 10:
		{
			System.out.println("print 10");
			break;
			
		}
		case 20:
		{
			System.out.println("print 20");
			break;
			
		}
		case 30:
		{
			System.out.println("print 30");
			break;
		}
		default :
			System.out.println("not on 20,30,40");
			
		}

	}

}
